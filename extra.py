print(a[3:7]) # (from front)3:7(from back) seperated the index values of the input value 

b = "Pragyan Khadka"
print(b[:7]) # prints only the first letter 

c = "Pragyan Khadka"
print(b[-5:-2]) # Negative index calls value from last 

